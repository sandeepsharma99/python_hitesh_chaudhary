# Determine if a fruit ripe ,overripe or unriped based on its color.(e.g.,Banana:Green-Unripe, yellow-Ripe, Brown-Overripe)  
fruit = "Banana"
color = "Green"
if fruit == "Banana":
    if color == "Green":
        print("unripe")
    elif color == "Yellow":
        print("Ripe")
    elif color == "Brown":
        print("Overripe")